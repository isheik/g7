                    <h2>Editing</h2>
                    <p>Now that you know how to select things and move around, let's talk about actually doing things. Outside of typing in text or viewing media, most of the work you do with your computer is going to fall under the generally umbrella of "editing." Editing is any time you're making adjustments to things - moving them, changing something about them, etc.</p>

                    <h3>Delete</h3>
                    <p>Let's start with the absolute basics of editing: the Delete function. There are generally two keys that you can use to delete something: <kbd>Delete</kbd> (which is often labeled "del") and <kbd>Backspace</kbd>.</p>

                    <p>The exact way that these behave is going to depend a bit on the application or part of the operating system that you're using. The <kbd>Delete</kbd> key should work basically anywhere. The <kbd>Backspace</kbd> is the sneaky one. If you're on a web page or navigating your file system, it generally changes its behavior and instead takes you to the last page you were on.</p>

                    <p>In a word processor or text editor, <kbd>Backspace</kbd> and <kbd>Delete</kbd> work very similarly, except that they delete in different directions. Backspace will delete stuff behind your cursor and move it backwards. <kbd>Delete</kbd> will stay put, and will delete stuff in front of it.</p>

                    <h3>More Advanced Editing</h3>
                    <p>With that out of the way, we can move on to some more advanced functionality that's built into just about every graphical operating system: copy and paste.</p>

                    <p>The idea is simple: using <em>copy</em>, you can take a copy of whatever you have selected and store it in a temporary virtual storage space called the "clipboard." When you want to insert a copy of this, you can use the <em>paste</em> command. The analogy comes from the old craft/art form of scrapbooking.</p>

                    <p>The keyboard shortcuts for these are <kbd>Ctrl</kbd> + <kbd>C</kbd> for copy and <kbd>Ctrl</kbd> + <kbd>V</kbd> or paste. These are bread-and-butter keyboard shortcuts that are well worth your time to remember.</p>

                    <p>There is one additional shortcut in this category: the <em>cut</em> command. By pressing <kbd>Ctrl</kbd> + <kbd>X</kbd>, you will copy what you have selected to the clipboard, and delete it from your workspace at the same time. This is used less often than copy, but it's really handy for moving things around when you're organizing documents, preparing presentations, etc.</p>

                    <h3>Undo and Redo</h3>
                    <p>Undo and redo are two functions that can be found in practically every modern application or operating system. The idea is simple: your computer keeps track of what your workspace looks like by taking little snapshots every time you do something to it. By using undo, you can go "back in time" to how your workspace looked beforehand.</p>

                    <p>The two shortcuts you need to know are <kbd>Ctrl</kbd> + <kbd>Z</kbd> for undo, and the less-used <kbd>Ctrl</kbd> + <kbd>Y</kbd>. While the most basic way to use these is to fix mistakes, it also makes it easy to try a large number of variations on an idea in quick succession, and then go back and pick your favorite one. You can also go back through your undo history, copy something, then fast-forward back to your most recent edit to paste that thing in.</p>

                    <p>One thing to keep in mind: your undo history generally isn't permenant. If you quit your application, your undo history is generally likely lost, so make sure you're happy with things before you exit.</p>

                    <p>Another thing to be aware of is that many applications use the undo history to restore files if they accidentally exit without saving.</p>
