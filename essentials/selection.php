                    <h2>Selection</h2>
                    <p>If you've used your computer for any length of time, you've probably run into situations where you want to work with several things at once. This might be several words in a text editor, or several files on your desktop. Whatever the specifics, the same basic principles apply.</p>

                    <h3>Selecting Multiple Objects</h3>
                    <p>There are several ways to select multiple objects. The easiest one, and the one we'll start with is using the <kbd>Shift</kbd> key.</p>

                    <p>If you hold shift while moving using the arrow keys, instead of releasing the previous object to focus on the new one, you'll instead focus on both objects at once. This is cumulative, meaning you can select as many objects in this manner as you like, provided they're all in the same general category (i.e., all icons, or text, or cells in a spreadsheet, etc.). We call this extending your selection.</p>

                    <p>One thing to be aware of is that your selection is generally rectangular. If you select several icons to the left, and then move down, your selection will form a box. That is, your selection will extend down from every icon that you already have selected.</p>

                    <h3>Combining the Keyboard and Mouse</h3>
                    <p>The most powerful extension to this feature is the ability to combine your keyboard selection keys with the mouse. If you hold shift and click, your selection will be extended from wherever it is to wherever you clicked. This is a fast and precise way to work with a large number of files at one time.</p>

                    <p>If you want to get a little more precise, you can use the <kbd>Ctrl</kbd> key. Holding <kbd>Ctrl</kbd> and clicking will add <em>or remove</em> one item from your selection. This is really great for removing just one or two files from a large selection, such as if you want to move everything <em>except</em> one file.</p>

                    <h3>Selecting All</h3>
                    <p>The last thing that's handy to know about, at least for selection, is the keyboard shortcut <kbd>Ctrl</kbd> + <kbd>A</kbd>. That means hold down the <kbd>Ctrl</kbd> key and press <kbd>A</kbd>. This will select everything in whatever area you've currently got in focus.</p>

                    <p>While most of our organization is handled by apps these days, occasionally you'll find you need to do some heavy reorganization of your computer's files, or interact with a large number of items in your working life. These shortcuts can make such chores fast and painless.</p>
