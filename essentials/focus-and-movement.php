                    <h2>Focus and Movement</h2>
                    <p>When you type, or use keyboard shortcuts, how does the computer decide where those end up? That's where the concept of focus comes in. If you have anything selected, those will be the objects that are in focus. If not, it's usually the last window that you interacted with.</p>

                    <p>Focus is usually indicated by some sort of highlighting. When you click an icon on your desktop or in a folder, it will be surrounded by a ring of little dots. This highlighting indicates that things you do with the keyboard will be done to this icon. The same concept applies across almost every facet of Windows 10.</p>

                    <p>Once you have focus, you can move your focus to adjacent objects by using the arrow keys. You'll see this behavior in word processors, menus, folders, and many other things in Windows. With these and a few other shortcuts, you can navigate your entire operating system without ever touching the touchscreen, mouse or trackpad.</p>

                    <h3>Releasing Focus</h3>
                    <p>If you want to let go of focus on something, you can generally press the <kbd>ESC</kbd> key. This is handy if you're in a menu and want to get back to your main program.</p>

                    <p>One other thing to keep in mind: when you're navigating the filesystem, double-clicking and pressing <kbd>Enter</kbd> are basically interchangable. So, you can press <kbd>Enter</kbd> to go into a selected folder, or to run a program or open a file.</p>
