                    <h2>Screen Layout</h2>
                    <p>Now let's talk about organizing your screen. Once you get into multitasking, you'll quickly find that it's important to be able to see multiple apps at a time. Windows 10 makes this especially easy for you, with an intuitive tiling system.</p>

                    <p><kbd>Win</kbd> + <kbd>Arrow Keys</kbd> are the main tools used to control your window layout. Pressing these will push your window to the left or right side, maximize it to cover the whole screen, or minimize it. This is easiest to understand by doing it, but here's some specifics to point you in the right direction.</p>

                    <p>First, <kbd>Win</kbd> + <kbd>Left Arrow</kbd> and <kbd>Win</kbd> + <kbd>Right Arrow</kbd> will push your window to the left or right sides, or pull it back to the middle. While on the sides, your window will take up exactly half of the screen real estate. From here, pressing <kbd>Win</kbd> + <kbd>Up Arrow</kbd> or <kbd>Win</kbd> + <kbd>Down Arrow</kbd>will shrink the window so that it takes up exactly 1/4 of the screen.</p>

                    <p>Next, while the window is free-floating, you can press <kbd>Win</kbd> + <kbd>Up Arrow</kbd> to maximize it, so it takes up the entire screen.</p>

                    <p>Again, this is something that takes a little bit of experimenting to get used to. Play with it, and you'll quickly find it makes intuitive sense and enables you to set up your workspace nicely.</p>
